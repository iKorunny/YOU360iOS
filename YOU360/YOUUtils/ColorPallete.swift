import Foundation
import UIKit

public final class ColorPallete {
    public static var appWhite: UIColor {
        return .white
    }
    
    public static var appBlack: UIColor {
        return .black
    }
    
    public static var appBlackSecondary: UIColor {
        return UIColor.color(from: "#2F2F2F")
    }
    
    public static var appWhiteSecondary: UIColor {
        return UIColor.color(from: "#F5F5F5")
    }
    
    public static var appPink: UIColor {
        return UIColor.color(from: "#F81C79")
    }
}
